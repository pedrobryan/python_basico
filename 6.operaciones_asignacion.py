x %= 4 # x = x % 4

print(x) # 2.0

# Operador "**="

x **= 3 # x = x ** 3

print(x) # 8.0

# Operador "-="

x -= 1 # x = x - 1

print(x)  # 7.0

# Operador "//="

x //= 2 # x = x // 2

print(x) # 3.0