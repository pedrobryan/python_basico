# Módulo - Uso de %, Nos da como resultado el residuo de una división entre enteros.

print("----- Operador módulo ----")

print(20 % 10) # 0

print(15 % 2) # 1

# Potencia - Uso de "**"

print("----- Operador potencia ----")

print(2 ** 3) # 8

print(10 ** 2) # 100

# División redondeada a un entero - uso de //

